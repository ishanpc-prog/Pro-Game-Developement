import pygame
import time

pygame.init()
WIDTH = 500
HEIGHT = 500

screen = pygame.display.set_mode([WIDTH,HEIGHT])
pygame.display.set_caption("Birthday card animation")
i1 = pygame.image.load("images/cake.jpg")
image1 = pygame.transform.scale(i1,(WIDTH,HEIGHT))
while True:
    font = pygame.font.SysFont("Times New Roman", 72)
    text1 = font.render("Happy", True, (150,235,235))
    text2 = font.render("Birthday", True, (150,235,235))
    screen.fill((120,120,120))
    screen.blit(image1,(0,0))
    screen.blit(text1,(25,400))
    screen.blit(text2,(230,400))
    pygame.display.update()
    time.sleep(6)
    i2 = pygame.image.load("images/balloonspaper.jpg")
    image2 = pygame.transform.scale(i2,(WIDTH,HEIGHT))
    font = pygame.font.SysFont("Times New Roman", 72)
    text3 = font.render("Happy",True,(150,235,235))
    text4 = font.render("Birthday",True,(150,235,235))
    screen.fill((120,120,120))
    screen.blit(image2,(0,0))
    screen.blit(text3,(150,150))
    screen.blit(text4,(135,250))
    pygame.display.update()
    time.sleep(6)
    i3 = pygame.image.load("images/presentbg.jpg")
    image3 = pygame.transform.scale(i3,(WIDTH,HEIGHT))
    font = pygame.font.SysFont("Times New Roman", 72)
    text5 = font.render("Happy",True,(150,235,235))
    text6 = font.render("Birthday",True,(150,235,235))
    screen.fill((120,120,120))
    screen.blit(image3,(0,0))
    screen.blit(text5,(20,20))
    screen.blit(text6,(230,20))
    pygame.display.update()
    time.sleep(6)
